#include<stdlib.h>
#include<time.h>
#include<stdio.h>
/**
 * main - This is the main function where execution starts
 *
 * Return: 0 after all code has been executed
 */
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;
	if (n > 0)
	{
		printf("%d is positive\n", n);
	}
	else if (n == 0)
	{
		printf("%d is zero\n", n);
	}
	else if (n < 0)
	{
		printf("%d is negative\n", n);
	}
	return (0);
}
